package com.avinnovz.sss.adapters;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.ArrayList;

/**
 * Created by jayan on 8/30/2016.
 */
public class DirectoryPagerAdapter extends FragmentPagerAdapter {

    private String tabTitles[] = new String[] {"Social Security Commission","Social Security System"};
    private ArrayList<Fragment> fragments;

    public DirectoryPagerAdapter(FragmentManager fm, ArrayList<Fragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return tabTitles.length;
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return tabTitles[position];
    }
}
